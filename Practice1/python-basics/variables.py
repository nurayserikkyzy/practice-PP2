

# 1. Создание переменной
x = 5
y = "Hello, World!"
print(x)
print(y)

# 2. Одновременное присваивание нескольких переменных
x, y, z = 1, 2, 3
print(x)
print(y)
print(z)

# 3. Переопределение значения переменной
x = 10
print(x)

# 4. Переменные чувствительны к регистру
name = "Alice"
Name = "Bob"
print(name)
print(Name)

# 5. Конкатенация строк с переменной
age = 16
print("I am " + str(age) + " years old.")
